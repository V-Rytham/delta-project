// 2 phases -> Project/development phase and 2nd is deployment phase
if (!process.env.NODE_ENV || process.env.NODE_ENV != "production") {
    require('dotenv').config() // all the variable in process.env
} 
const express = require("express");
const app = express();
const mongoose = require("mongoose");
const path = require("path");
const methodOverride = require("method-override");
const ejsMate = require("ejs-mate");
const ExpressError = require("./utils/ExpressError.js");
const listings = require("./routes/listing.js");
const reviews = require("./routes/review.js");
const session = require("express-session");
const MongoStore = require("connect-mongo").default;
const flash = require("connect-flash");
const userRouter = require("./routes/user.js");
const passport = require("passport");
const LocalStrategy = require("passport-local");
const passportLocalMongoose = require("passport-local-mongoose");
const User = require("./Models/user.js");

const dbUrl = process.env.ATLAS_DB_URL;

const store = MongoStore.create({
    mongoUrl: dbUrl,
    crypto: {
        secret: process.env.SECRET,
    },
    touchAfter: (24*3600),
});

store.on("error", (err) => console.log("Error in Mongo Session store", err));

const sessionOptions = {
    secret : process.env.SECRET,
    resave: false,
    saveUninitialized: false,
    cookie : {
        expires: new Date(Date.now() + (7 * 24 * 60 * 60 * 1000)),  
        maxAge: 7 * 24 * 60 * 60 * 1000,
        httpOnly: true // To prevent XSS cookie theft //Ill show where your data is saced
    }
};


main().catch (err => {
    console.log(dbUrl);
    console.log(err);
});

async function main() {
    await mongoose.connect(dbUrl);
};




app.use(session(sessionOptions));
app.use(passport.initialize());
app.use(passport.session());

passport.use(new LocalStrategy(User.authenticate()));
passport.serializeUser(User.serializeUser());
passport.deserializeUser(User.deserializeUser());
app.use(flash()); // understand why is this before routes
app.use((req, res, next) => {
    res.locals.success = req.flash("success");
    res.locals.error = req.flash("error");
    res.locals.currUser = req.user;
    next();
});
app.set("view engine", "ejs");
app.set("views", path.join(__dirname, 'views'));
app.use(express.urlencoded({extended: true}));
app.use(methodOverride("_method"));
app.engine("ejs", ejsMate); // similar to includes
app.use(express.static(path.join(__dirname, 'public')));
app.use("/listings", listings);
app.use("/listings/:id/reviews", reviews);
app.use("/user", userRouter);

// app.get("/", (req, res) => res.send("Root Page"));
app.listen(8080, () => {
    console.log("listening...");
});

app.use((req, res, next) => {
    next(new ExpressError(404, "Page not Found"));
});

